

public class Divisionoperation {
    public static void main(String[] args) {
        int a = 15;
        int b = 0;

        if (b == 0) {
            System.out.println("Error: Division by zero is not allowed");
        } else {
            int div = a / b;
            System.out.println("Division: " + div);
        }
    }
}
